#include "libft.h"
char *ft_substr(char const *s, unsigned int start, size_t len)
{
	char * str;
	size_t	i;
	size_t slen;

	i = 0;
	slen = ft_strlen(s);
	if (!s)
		return (NULL);
	if (start >= slen)
		return (ft_strdup(""));
	if (len > slen - start)
		len = slen - start;
	str = malloc((sizeof(char) * len) + 1);
	if (!str)
		return(NULL);
	while (i < len)
	{
		str[i] = s[start + i];
		i++;
	}
	str[i] = '\0';
	return (str);
}